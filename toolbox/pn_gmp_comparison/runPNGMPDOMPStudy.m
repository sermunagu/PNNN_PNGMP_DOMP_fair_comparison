function study = runPNGMPDOMPStudy(x, y, split, cfg)
% runPNGMPDOMPStudy - Orchestrate the six linear DOMP comparison models.
% Selection uses the internal identification split, final fits use all
% identification rows, and evaluation uses the complete signal.

studyTimer = tic;
x = x(:);
y = y(:);
requiredFields = {'internalTrainIndices','internalValidationIndices', ...
    'identificationIndices','fullSignalIndices'};
if ~all(isfield(split, requiredFields)) || ...
        ~all(ismember(split.identificationIndices, ...
        split.fullSignalIndices)) || ...
        ~all(ismember(split.internalTrainIndices, ...
        split.identificationIndices)) || ...
        ~all(ismember(split.internalValidationIndices, ...
        split.identificationIndices)) || ...
        ~isempty(intersect(split.internalTrainIndices, ...
        split.internalValidationIndices)) || ...
        ~isequal(sort([split.internalTrainIndices; ...
        split.internalValidationIndices]), ...
        sort(split.identificationIndices)) || ...
        ~isequal(split.fullSignalIndices(:), (1:numel(x)).')
    error('runPNGMPDOMPStudy:InvalidSplit', ...
        'The supplied identification/full-signal protocol is invalid.');
end

[design, selection] = preparePNGMPDOMPSelection(x, y, split, cfg);
models = fitPNGMPDOMPModels(x, design, selection, cfg);
evaluation = evaluatePNGMPDOMPModels( ...
    x, y, design, selection, models, cfg);

[regressorStructureSummary, ~] = analyzeRegressorStructure( ...
    design.gmpManager.regPopulation, design.supportBase);
historyLabels = ["INTERNAL_TRAIN complex path"; ...
    "IDENTIFICATION complex path"; ...
    "INTERNAL_TRAIN reduced PN-IQ"; ...
    "IDENTIFICATION reduced PN-IQ"];
historyTables = { ...
    summarizeDOMPHistory(design.trainPathHistory); ...
    summarizeDOMPHistory(design.identificationPathHistory); ...
    summarizeDOMPHistory(design.reducedTrainSelection.dompHistory); ...
    summarizeDOMPHistory(models.reducedSelection.dompHistory)};
dompHistorySummary = vertcat(historyTables{:});
dompHistorySummary = addvars(dompHistorySummary, historyLabels, ...
    'Before', 1, 'NewVariableNames', 'Stage');

study = struct( ...
    'comparisonResults', evaluation.comparisonResults, ...
    'complexityFLOPs', evaluation.complexityFLOPs, ...
    'flopConvention', evaluation.flopConvention, ...
    'selectedHyperparameters', evaluation.selectedHyperparameters, ...
    'dompHistorySummary', dompHistorySummary, ...
    'regressorStructureSummary', regressorStructureSummary, ...
    'supports', struct( ...
        'supportDOMP', design.supportBase, ...
        'supportParameterMatchedDOMP', design.supportMatched, ...
        'reducedFeatureSupportDOMP', models.reducedSupport, ...
        'supportTrainBase', design.supportTrainBase, ...
        'supportTrainParameterMatched', design.supportTrainMatched), ...
    'dompHistories', struct( ...
        'internalTrainComplex', design.trainPathHistory, ...
        'identificationComplex', design.identificationPathHistory, ...
        'internalTrainReducedPNIQ', ...
            design.reducedTrainSelection.dompHistory, ...
        'identificationReducedPNIQ', models.reducedSelection.dompHistory), ...
    'identificationPredictions', evaluation.identificationPredictions, ...
    'fullSignalPredictions', evaluation.fullSignalPredictions, ...
    'targetIndependentPNIQParameters', ...
        design.targetIdentificationParameters, ...
    'internalTrainIndependentPNIQParameters', ...
        design.targetTrainParameters, ...
    'populationSize', design.populationSize, ...
    'equivalenceRelativeError', evaluation.equivalenceRelativeError, ...
    'totalTimeSeconds', toc(studyTimer));
end
